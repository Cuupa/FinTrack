#!/usr/bin/env node
// Stamps a fresh cache-busting version into public/sw-version.js before every
// build, so a deploy that changes the app shell automatically invalidates the
// previous deploy's service-worker cache (see public/sw.js's header comment
// and OFFLINE_DESIGN.md §5.3/§5 risk 1, "fold the build id into CACHE").
//
// public/sw-version.js is generated, not committed (gitignored): writing it
// unconditionally into the tracked public/sw.js would dirty the working tree
// on every single build. sw.js instead `importScripts()`s this sibling file
// at runtime and falls back to a static "dev" version if it's missing (e.g.
// a checkout that never ran a build), see public/sw.js for that fallback.
//
// Version source: the current commit's short SHA when running inside a git
// checkout (the common case, Vercel builds from a git ref), else a
// timestamp so the file still changes on every build in a non-git deploy.

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));

function resolveVersion() {
  try {
    const sha = execSync("git rev-parse --short HEAD", {
      stdio: ["ignore", "pipe", "ignore"],
    })
      .toString()
      .trim();
    if (sha) return sha;
  } catch {
    // No git available (e.g. built from a source tarball), fall through.
  }
  return `t${Date.now()}`;
}

const version = resolveVersion();
const outPath = join(__dirname, "..", "public", "sw-version.js");
const contents = `// Generated by scripts/generate-sw-version.mjs at build time. Do not edit or commit.\nself.SW_BUILD_VERSION = ${JSON.stringify(version)};\n`;

writeFileSync(outPath, contents);
console.log(`[generate-sw-version] wrote ${outPath} (SW_BUILD_VERSION=${version})`);
